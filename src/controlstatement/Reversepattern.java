package controlstatement;

public class Reversepattern {

	public static void main(String[] args) {
		int n=6;
		for(int i=1; i<=6;i++)
		{
			for(int j=1;j<=n;j++)
			{
				System.out.print(j+" ");
				
			}
			System.out.println();
			n--;
		}

	}

}














